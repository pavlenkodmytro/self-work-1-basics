"""
Завдання 5: Пошук мінімуму або максимуму залежно від парності числа

Програма знаходить:
- Мінімальне значення з трьох чисел, якщо перше число ПАРНЕ
- Максимальне значення з трьох чисел, якщо перше число НЕПАРНЕ

Парне число - ціле число, яке ділиться на 2 без залишку (наприклад: 2, 4, 6, 8...)
Непарне число - ціле число, яке НЕ ділиться на 2 без залишку (наприклад: 1, 3, 5, 7...)
"""


def is_even(number):
    """
    Перевіряє, чи є число парним.

    Число парне, якщо воно ділиться на 2 без залишку.
    Для перевірки використовується оператор % (модуль) - залишок від ділення.

    Args:
        number (int): Число для перевірки

    Returns:
        bool: True якщо число парне, False якщо непарне

    Example:
        >>> is_even(4)
        True  # 4 ділиться на 2 без залишку (4 % 2 = 0)

        >>> is_even(7)
        False  # 7 не ділиться на 2 без залишку (7 % 2 = 1)

        >>> is_even(0)
        True  # 0 ділиться на 2 без залишку (0 % 2 = 0)
    """
    # Оператор % повертає залишок від ділення
    # Якщо число ділиться на 2 без залишку, то воно парне
    return number % 2 == 0


def find_minimum(num1, num2, num3):
    """
    Знаходить мінімальне значення серед трьох чисел.

    Args:
        num1 (float): Перше число
        num2 (float): Друге число
        num3 (float): Третє число

    Returns:
        float: Мінімальне значення

    Example:
        >>> find_minimum(5, 2, 8)
        2

        >>> find_minimum(-3, 10, 0)
        -3
    """
    # Використовуємо вбудовану функцію min() для знаходження мінімуму
    minimum = min(num1, num2, num3)

    print(f"Пошук мінімального значення:")
    print(f"  Числа: {num1}, {num2}, {num3}")
    print(f"  Мінімум: {minimum}")

    return minimum


def find_maximum(num1, num2, num3):
    """
    Знаходить максимальне значення серед трьох чисел.

    Args:
        num1 (float): Перше число
        num2 (float): Друге число
        num3 (float): Третє число

    Returns:
        float: Максимальне значення

    Example:
        >>> find_maximum(5, 2, 8)
        8

        >>> find_maximum(-3, 10, 0)
        10
    """
    # Використовуємо вбудовану функцію max() для знаходження максимуму
    maximum = max(num1, num2, num3)

    print(f"Пошук максимального значення:")
    print(f"  Числа: {num1}, {num2}, {num3}")
    print(f"  Максимум: {maximum}")

    return maximum


def process_numbers_by_parity(first_number, second_number, third_number):
    """
    Обробляє три числа залежно від парності першого числа.

    Логіка роботи:
    - Якщо перше число парне → шукаємо мінімум серед трьох чисел
    - Якщо перше число непарне → шукаємо максимум серед трьох чисел

    Args:
        first_number (int): Перше число (визначає операцію)
        second_number (float): Друге число
        third_number (float): Третє число

    Returns:
        tuple: (результат, тип операції)
            - результат: знайдене мінімальне або максимальне значення
            - тип операції: "мінімум" або "максимум"
    """
    print("=" * 70)
    print("АНАЛІЗ ПЕРШОГО ЧИСЛА")
    print("=" * 70)
    print()

    # Перевіряємо парність першого числа
    first_is_even = is_even(first_number)

    # Виводимо інформацію про парність
    print(f"Перше число: {first_number}")
    if first_is_even:
        parity_text = "ПАРНЕ"
        operation_text = "мінімальне"
    else:
        parity_text = "НЕПАРНЕ"
        operation_text = "максимальне"

    print(f"Тип числа: {parity_text}")
    print(f"Операція: шукаємо {operation_text} значення")
    print()

    print("=" * 70)
    print("ОБЧИСЛЕННЯ РЕЗУЛЬТАТУ")
    print("=" * 70)
    print()

    # Виконуємо відповідну операцію залежно від парності
    if first_is_even:
        # Перше число парне → шукаємо мінімум
        result = find_minimum(first_number, second_number, third_number)
        operation = "мінімум"
    else:
        # Перше число непарне → шукаємо максимум
        result = find_maximum(first_number, second_number, third_number)
        operation = "максимум"

    return result, operation


